import pandas as pd
import numpy as np
import joblib
import matplotlib.pyplot as plt
import seaborn as sns
from xgboost import XGBClassifier
from sklearn.metrics import classification_report, confusion_matrix

# 1. Reload Testing Data Pools & Scaler
X_test_scaled = np.load('X_test_scaled.npy')
y_test = np.load('y_test.npy')
X_test_raw = pd.read_csv('X_test_raw.csv')
classes = np.load('label_classes.npy', allow_pickle=True)

svm_model = joblib.load('svm_industrial_model.pkl')
rf_model = joblib.load('rf_industrial_model.pkl')
xgb_model = XGBClassifier(); xgb_model.load_model('xgb_industrial_model.json')

models_dict = {
    "SVM Baseline": svm_model,
    "Random Forest Ensemble": rf_model,
    "XGBoost Gradient Boosting": xgb_model
}

# ==========================================
# SUB-PART A: LOG TEST PERFORMANCE MATRICES
# ==========================================
print("="*80)
print("             DEPLOYMENT TESTING REPORT BREAKDOWNS             ")
print("="*80)

for model_name, model in models_dict.items():
    y_pred = model.predict(X_test_scaled)
    print(f"\n[Algorithm Testing Slices Deployment]: {model_name}")
    print(classification_report(y_test, y_pred, target_names=classes, digits=2))
    print("="*80)

# ==========================================
# SUB-PART B: RENDER STANDALONE TEST CMs DYNAMICALLY
# ==========================================
plt.rcParams['font.weight'] = 'bold'
plt.rcParams['axes.labelweight'] = 'bold'

for model_name, model in models_dict.items():
    y_pred = model.predict(X_test_scaled)
    cm = confusion_matrix(y_test, y_pred)

    fig, ax = plt.subplots(figsize=(5.5, 5))
    sns.heatmap(cm, annot=True, fmt='d', cmap='GnBu', cbar=True, xticklabels=classes, yticklabels=classes,
                annot_kws={'size': 14, 'weight': 'bold'}, linewidths=1.5, linecolor='white', square=True)

    ax.set_title(f'{model_name} Test Confusion Matrix\n', fontsize=12, fontweight='bold')
    ax.set_xlabel('\nPredicted Condition Label', fontsize=11, fontweight='bold')
    ax.set_ylabel('Actual Ground Truth Label\n', fontsize=11, fontweight='bold')
    plt.tight_layout()
    plt.show()

# ==========================================
# SUB-PART C: EXTRACT PHYSICAL TORQUE WINDOW BOUNDARIES
# ==========================================
print("\n" + "="*115)
print("      EXTRACTED PHYSICAL TORQUE VALUE RANGES (Nm) THAT TRIGGERED ERROR CLASSIFICATIONS")
print("="*115)

torque_cols = ['J1_torque', 'J2_torque', 'J3_torque', 'J4_torque', 'J5_torque', 'J6_torque']

# Find what integer corresponds to 'Abnormal' or 'Error' status
error_label_string = 'Abnormal' if 'Abnormal' in list(classes) else 'Error'
error_val = list(classes).index(error_label_string)

boundary_records = []

for model_title, baseline_model in models_dict.items():
    short_name = "SVM" if "SVM" in model_title else ("Random Forest" if "Forest" in model_title else "XGBoost")
    preds = baseline_model.predict(X_test_scaled)

    # Filter only samples where the model predicted an anomaly state
    error_mask = (preds == error_val)
    error_frames = X_test_raw[error_mask]

    for t_axis in torque_cols:
        min_val = error_frames[t_axis].min() if len(error_frames) > 0 else np.nan
        max_val = error_frames[t_axis].max() if len(error_frames) > 0 else np.nan
        boundary_records.append({
            'Algorithm': short_name,
            'Torque Joint': t_axis.split('_')[0],
            'Min Torque Bound (Nm)': round(min_val, 4),
            'Max Torque Bound (Nm)': round(max_val, 4)
        })

boundary_df = pd.DataFrame(boundary_records)
final_table = boundary_df.pivot(index='Torque Joint', columns='Algorithm', values=['Min Torque Bound (Nm)', 'Max Torque Bound (Nm)'])

print(final_table.to_string())
final_table.to_csv('physical_error_ranges.csv')
print("\n-> CSV summary table successfully stored as 'physical_error_ranges.csv'")