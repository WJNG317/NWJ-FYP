import pandas as pd
import numpy as np
import joblib
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler, LabelEncoder

# 1. Load the Dataset (Ensure fanuc_data(Data)2.csv is uploaded to your Colab files panel)
df = pd.read_csv('fanuc_data(Data)2.csv')

# 2. Setup Telemetry Feature Framework (13 mechatronic features)
features = ['J1_pos', 'J2_pos', 'J3_pos', 'J4_pos', 'J5_pos', 'J6_pos',
            'J1_torque', 'J2_torque', 'J3_torque', 'J4_torque', 'J5_torque', 'J6_torque',
            'program_speed']
X = df[features]
y = df['Status']

# 3. Label Encoding
le = LabelEncoder()
y_encoded = le.fit_transform(y)
np.save('label_classes.npy', le.classes_)

# 4. Stratified Split (80% Train, 20% Test)
X_train, X_test, y_train, y_test = train_test_split(X, y_encoded, test_size=0.2, random_state=42, stratify=y_encoded)

# 5. Standard Feature Scaling
scaler = StandardScaler()
X_train_scaled = scaler.fit_transform(X_train)
X_test_scaled = scaler.transform(X_test)

# 6. Save Arrays and Scaler Objects to local environment
joblib.dump(scaler, 'telemetry_scaler.pkl')
np.save('X_train_scaled.npy', X_train_scaled)
np.save('y_train.npy', y_train)
np.save('X_test_scaled.npy', X_test_scaled)
np.save('y_test.npy', y_test)
X_test.to_csv('X_test_raw.csv', index=False)

print("="*50)
print("PART 1: PREPROCESSING COMPLETED & SCALER EXPORTED")
print("="*50)
print(f"Training features shape : {X_train_scaled.shape}")
print(f"Testing features shape  : {X_test_scaled.shape}")

